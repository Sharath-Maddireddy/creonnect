    @field_validator("hook_strength_score", mode="before")
    @classmethod
    def _clamp_hook_strength(cls, value: float | int | str | None) -> float | None:
        if value is None:
            return None
        try:
            numeric = float(value)
        except (TypeError, ValueError):
            return None
        return max(0.0, min(1.0, numeric))


