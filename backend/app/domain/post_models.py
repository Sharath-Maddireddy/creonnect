"""Canonical domain models for Single Post Insights."""

from __future__ import annotations

from datetime import datetime
from typing import Literal

from pydantic import BaseModel, ConfigDict, Field, ValidationInfo, field_validator


# -----------------------------
# Core metrics
# -----------------------------


class CoreMetrics(BaseModel):
    """Raw Instagram metrics captured from the source system for a single post."""

    model_config = ConfigDict(extra="forbid")

    reach: int | None = Field(default=None, description="Unique accounts reached by the post.")
    impressions: int | None = Field(default=None, description="Total impressions generated by the post.")
    likes: int | None = Field(default=None, description="Total likes on the post.")
    comments: int | None = Field(default=None, description="Total comments on the post.")
    shares: int | None = Field(default=None, description="Total shares of the post.")
    saves: int | None = Field(default=None, description="Total saves/bookmarks on the post.")
    profile_visits: int | None = Field(default=None, description="Profile visits attributed to the post.")
    website_taps: int | None = Field(default=None, description="Website taps attributed to the post.")
    source_engagement_rate: float | None = Field(
        default=None,
        description="Top-level raw engagement rate provided by source systems.",
    )


